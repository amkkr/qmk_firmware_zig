/// RP2040 BootROM CRC32を計算する
///
/// RP2040 BootROMの CRC32は以下の手順で計算される（pico-sdk pad_checksum参照）:
/// 1. 各入力バイトのビットを反転
/// 2. 標準CRC32（初期値 seed ^ 0xFFFFFFFF）を計算
/// 3. 結果に ^ 0xFFFFFFFF
/// 4. 結果の32ビットをビット反転
///
/// seed=0の場合、これはCRC-32/MPEG-2（多項式0x04C11DB7、
/// init=0xFFFFFFFF、refin=false、refout=false、xorout=0x00000000）と等価になる。
fn bootromCrc32(payload: []const u8) u32 {
    @setEvalBranchQuota(10000);
    var crc: u32 = 0xFFFFFFFF;
    for (payload) |byte| {
        crc ^= @as(u32, byte) << 24;
        for (0..8) |_| {
            if (crc & 0x80000000 != 0) {
                crc = (crc << 1) ^ 0x04C11DB7;
            } else {
                crc = crc << 1;
            }
        }
    }
    return crc;
}

// ビルド時CRC32検証: データの整合性をコンパイル時に保証
comptime {
    std.debug.assert(verifyCrc32());
}

/// Boot2データのCRC32を検証する
pub fn verifyCrc32() bool {
    const payload = data[0..crc32_offset];
    const stored_crc = std.mem.readInt(u32, data[crc32_offset..boot2_size], .little);
    const computed_crc = bootromCrc32(payload);
    return computed_crc == stored_crc;
}

// ==========================================================
// テスト
// ==========================================================

test "Boot2バイナリサイズが256バイト" {
    try std.testing.expectEqual(@as(usize, 256), data.len);
}

test "Boot2のCRC32チェックサムが正しい" {
    try std.testing.expect(verifyCrc32());
}

test "Boot2の先頭バイトが有効なThumb命令" {
    // 先頭2バイトは PUSH {LR} 命令 (0xB500, little-endian)
    try std.testing.expectEqual(@as(u8, 0x00), data[0]);
    try std.testing.expectEqual(@as(u8, 0xb5), data[1]);
}

test "Boot2の末尾CRC32が非ゼロ" {
    const crc_bytes = data[crc32_offset..boot2_size];
    const crc_val = std.mem.readInt(u32, crc_bytes, .little);
    try std.testing.expect(crc_val != 0);
}
