//! Boot2 - RP2040 第2段ブートローダー
//!
//! RP2040はフラッシュからの起動時、内蔵BootROM（マスクROM）が
//! フラッシュ先頭256バイトのBoot2コードを実行してSSI（SPI）コントローラを
//! XIPモード用に初期化する。
//!
//! このモジュールはW25Q080/W25Q16JV互換の汎用SPI Flash向けBoot2バイナリを提供する。
//! ソース: pico-sdk boot2_w25q080.S 相当（プリコンパイル済みバイナリ）
//!
//! 仕様:
//! - 配置アドレス: 0x10000000（XIPベース）
//! - サイズ: 256バイト固定
//! - 末尾4バイト: CRC32チェックサム（RP2040 BootROMが検証）

